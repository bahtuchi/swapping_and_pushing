char	*ft_strrchr(const char *s, int c)
{
	char	*cptr;

	cptr = (void *)0;
	while (*s)
	{
		if (*s == (char)c)
			cptr = (char *)s;
		++s;
	}
	if (!(char)c)
		cptr = (char *)s;
	return (cptr);
}
